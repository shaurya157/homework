class CatsController < ApplicationController

  before_filter :validate_creating_cats, only: :create
  before_filter :validate_owner, only: [:edit, :update]

  def index
    @cats = Cat.all
    render :index
  end

  def show
    @cat = Cat.find(params[:id])
    render :show
  end

  def new
    @cat = Cat.new
    render :new
  end

  def create
    @cat = Cat.new(cat_params)
    @cat.user_id = current_user.id

    if @cat.save
      redirect_to cat_url(@cat)
    else
      flash.now[:errors] = @cat.errors.full_messages
      render :new
    end
  end

  def edit
    @cat = Cat.find(params[:id])
    render :edit
  end

  def update
    @cat = Cat.find(params[:id])
    if @cat.update_attributes(cat_params)
      redirect_to cat_url(@cat)
    else
      flash.now[:errors] = @cat.errors.full_messages
      render :edit
    end
  end

  def validate_creating_cats
    if !current_user
      flash[:errors] = ['Login to create a new cat!']
      redirect_to cats_url
    end
  end

  def validate_owner
    if current_user.nil? || !current_user.cats.find_by(id: params[:id])
      flash[:errors] = ['That aint your cat!']
      redirect_to cats_url
    end
  end

  private

  def cat_params
    params.require(:cat)
      .permit(:age, :birth_date, :color, :description, :name, :sex)
  end
end
